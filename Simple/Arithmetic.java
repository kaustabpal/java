class Arithmetic
{
public static void main(String[] args)
{
int a,b,c,d;
a=10;
b=20;
c=30;
d=a+b+c;
System.out.println("The sum of "+a+", "+b+" and "+c+" is: "+d);
d=a*b*c;
System.out.println("The product of "+a+", "+b+" and "+c+" is: "+d);
d=c/b;
System.out.println("The division of "+c+" and "+b+" is: "+d);
d=c-b;
System.out.println("The difference between "+c+" and "+b+" is: "+d);
}
}
